import UIKit

class FontConstant: NSObject {
    
    class func FONT_HELVETICA (fontSize : CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue", size: fontSize)!
    }
    
    class func FONT_HELVETICA_BOLD (fontSize : CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Bold", size: fontSize)!
    }
    
    class func FONT_HELVETICA_BOLD_ITALIC (fontSize : CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeueBold-Italic", size: fontSize)!
    }
    
    class func FONT_HELVETICA_ITALIC (fontSize : CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Italic", size: fontSize)!
    }
    
    class func FONT_HELVETICANEUE (fontSize : CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue", size: fontSize)!
    }
    
    class func FONT_HELVETICANEUE_BOLD (fontSize : CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Bold", size: fontSize)!
    }
    
    class func FONT_HELVETICANEUE_BOLD_ITALIC (fontSize : CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeueBold-Italic", size: fontSize)!
    }
    
    class func FONT_HELVETICANEUE_ITALIC (fontSize : CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Italic", size: fontSize)!
    }
}
